package sample;

public class RandomNumber {
    public int randnum;
    public RandomNumber() {
        this.randnum = randnum;
    }

    public void setRandnum(int randnum) {
        this.randnum = randnum;
    }

    public int getRandnum() {
        return randnum;
    }

    @Override
    public String toString() {
        return Integer.toString(randnum);
    }
}
